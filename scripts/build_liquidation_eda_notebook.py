"""Build the final liquidation EDA notebook from generated full-data artifacts.

The expensive EDA pipeline writes compact CSV tables and PNG figures under
``reports/liquidation_eda``.  This script turns those artifacts into an
executed, readable notebook report without recomputing the 1B+ row pipeline.
"""

